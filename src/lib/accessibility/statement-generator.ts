/**
 * Accessibility Statement Draft Generator
 *
 * Generates DRAFT accessibility statements for user review.
 *
 * CRITICAL LANGUAGE CONSTRAINTS:
 *  - Every generated statement MUST include the DRAFT disclaimer.
 *  - Never use: "guaranteed compliance", "certified", "legally compliant",
 *    "immune from fines", "100% compliant", "government certification".
 *  - Always include: "DRAFT — Review before publication",
 *    "This statement was generated as a starting template",
 *    "Manual review required before publication".
 */

/** Always present in every generated statement — never remove. */
export const STATEMENT_DISCLAIMER =
  'DRAFT — Review before publication. This statement was generated by WebScore as a starting template. ' +
  'It must be reviewed by a qualified person before publication. WebScore does not provide legal advice. ' +
  'Automated testing alone cannot certify conformance with accessibility standards.';

export interface StatementKnownIssue {
  title:             string;
  impact:            string;
  alternativeAccess?: string;
}

export interface AccessibilityStatementContent {
  organizationName:   string;
  siteName:           string;
  siteUrl:            string;
  jurisdictionId:     string;
  standardName:       string;
  assessmentDate:     string;
  currentStatusWording: string;
  knownIssues:        StatementKnownIssue[];
  remediationPlan:    string;
  contactEmail?:      string;
  contactFormUrl?:    string;
  contactPhone?:      string;
  postalAddress?:     string;
  supportUrl?:        string;
  statementDate:      string;
  nextReviewDate:     string;
  templateVersion:    string;
  disclaimer:         string;
}

export interface StatementTemplate {
  jurisdictionId:   string;
  version:          string;
  requiredSections: string[];
  reviewInterval:   string;
  disclaimer:       string;
}

