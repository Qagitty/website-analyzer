/** Fallback template used when jurisdiction has no specific template. */
const FALLBACK_TEMPLATE: StatementTemplate = {
  jurisdictionId:   'generic',
  version:          '2026.1',
  requiredSections: ['scope', 'conformance_status', 'known_issues', 'contact', 'preparation_date'],
  reviewInterval:   '12 months',
  disclaimer:       STATEMENT_DISCLAIMER,
};

export interface GenerateStatementOpts {
  profile: {
    name:             string;
    targetMarkets:    string[];
    selectedStandards: string[];
  };
  assessment: {
    completedAt?:      string;
    coveragePercent:   number;
    riskResult?:       { label: string } | null;
  };
  jurisdiction: {
    id:                string;
    name:              string;
    technicalStandard?: string;
  };
  /** Open findings to list in the known-issues section (max 20). */
  knownFindings:  { title: string; impact: string }[];
  contactInfo:    Partial<AccessibilityStatementContent>;
}

/**
 * Generate a DRAFT accessibility statement for user review.
 *
 * The output always includes STATEMENT_DISCLAIMER and never claims
 * certification, legal immunity, or government-authority approval.
 */
export function generateStatementDraft(opts: GenerateStatementOpts): AccessibilityStatementContent {
  const now = new Date();
  const reviewDate = new Date(now);
  reviewDate.setFullYear(reviewDate.getFullYear() + 1);

  const template =
    STATEMENT_TEMPLATES[opts.jurisdiction.id.toLowerCase()] ?? FALLBACK_TEMPLATE;

  const knownIssues: StatementKnownIssue[] = opts.knownFindings
    .slice(0, 20)
    .map((f) => ({
      title:             f.title,
      impact:            f.impact,
      alternativeAccess: 'Please contact us for an accessible alternative.',
    }));

  // Neutral wording — never claims conformance or legal status
  const currentStatusWording =
    opts.assessment.coveragePercent >= 80
      ? 'Partially conformant — some content does not fully conform to the applicable standard. Known accessibility issues are listed below.'
      : 'Non-conformant or insufficient test coverage — the automated assessment did not cover enough of the site to determine conformance status. Manual review is required.';

  return {
    organizationName:     opts.contactInfo.organizationName ?? '[Organization Name]',
    siteName:             opts.profile.name,
    siteUrl:              opts.contactInfo.siteUrl ?? '[Site URL]',
    jurisdictionId:       opts.jurisdiction.id,
    standardName:
      opts.jurisdiction.technicalStandard ??
      opts.profile.selectedStandards[0] ??
      'WCAG 2.1 AA',
    assessmentDate:
      opts.assessment.completedAt?.split('T')[0] ??
      now.toISOString().split('T')[0],
    currentStatusWording,
    knownIssues,
    remediationPlan:
      opts.contactInfo.remediationPlan ??
      'We are actively working to resolve identified accessibility issues. ' +
      'Please contact us for updates on our remediation timeline.',
    contactEmail:    opts.contactInfo.contactEmail,
    contactFormUrl:  opts.contactInfo.contactFormUrl,
    contactPhone:    opts.contactInfo.contactPhone,
    postalAddress:   opts.contactInfo.postalAddress,
    supportUrl:      opts.contactInfo.supportUrl,
    statementDate:      now.toISOString().split('T')[0],
    nextReviewDate:     reviewDate.toISOString().split('T')[0],
    templateVersion:    template.version,
    disclaimer:         STATEMENT_DISCLAIMER,
  };
}
